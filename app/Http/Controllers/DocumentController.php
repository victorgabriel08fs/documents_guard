    /**
     * Store a newly created resource in storage.
     *
     * @param  \App\Http\Requests\StoreDocumentRequest  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {

        if ($request->document) {
            $document_urn = $request->file('document')->store('documents/' . auth()->user()->id);
            $data['path'] = $document_urn;
        }

        $data['name'] = $request->name;
        $data['password'] = bcrypt($request->password);
        $data['user_id'] = auth()->user()->id;

        $data['hash'] = Hash::make(auth()->user()->id);

        Document::create($data);

        return redirect()->route('document.create')->withErrors(['success' => 'Envio confirmado! Guarda seu c√≥digo para ter acesso a ele: ' . $data['hash']]);
    }

